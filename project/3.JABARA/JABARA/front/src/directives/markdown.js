const rules = [
  [/#{1}\s?([^\n]+)/g, "<h1>$1</h1>"],
  [/#{1}\s?([^\n]+)\n/g, "<h1>$1</h1>"],
  [/#{2}\s?([^\n]+)/g, "<h2>$1</h2>"],
  [/#{2}\s?([^\n]+)\n/g, "<h2>$1</h2>"],
  [/#{3}\s?([^\n]+)/g, "<h3>$1</h3>"],
  [/#{3}\s?([^\n]+)\n/g, "<h3>$1</h3>"],
  [/#{4}\s?([^\n]+)/g, "<h4>$1</h4>"],
  [/#{4}\s?([^\n]+)\n/g, "<h4>$1</h4>"],
  [/#{5}\s?([^\n]+)/g, "<h5>$1</h5>"],
  [/#{5}\s?([^\n]+)\n/g, "<h5>$1</h5>"],
  [/#{6}\s?([^\n]+)/g, "<h6>$1</h6>"],
  [/#{6}\s?([^\n]+)\n/g, "<h6>$1</h6>"],
  [/([^\n]+)\n\s+=+/g, "<h1>$1</h1><hr/>"],
  [/([^\n]+)\n\s+-+/g, "<h2>$1</h2><hr/>"],
  //   [/\*\*([^\*\n]+)\*\*/g, "<b>$1</b>"],
  //   [/\*([^\*\n]+)\*/g, "<i>$1</i>"],
  //   [/__([^_]+)__/g, "<b>$1</b>"],
  //   [/_([^_]+)_/g, "<i>$1</i>"],
  //   [/((\n\d\..+)+)/g, "<ol>$1</ol>"],
  //   [/((\n\*.+)+)/g, "<ul>$1</ul>"],
  //   [/\n\d\.([^\n]+)/g, "<li>$1</li>"],
  //   [/\n\*([^\n]+)/g, "<li>$1</li>"],
  //   [/\!\[([^\]]+)\]\(([^\)]+)\s\"([^\"\)]+)\"\)/g, "<li>$1</li>"],
];

export default {
  bind(el) {
    // const text = el.textContent;
    let html = el.textContent;
    rules.forEach(([rule, template]) => {
      html = html.replace(rule, template);
    });
    // html = "###### HELLO WORLD";

    el.innerHTML = html;
  },
};
